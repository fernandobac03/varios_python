"""
# Ejercicio 4
Pedir dos números al usuario, y hacer las 4 operaciones básicas
"""

numero_1 = int(input("Ingrese el primer número: "))
numero_2 = int(input("Ingrese el segundo número: "))

#Suma
print(f"La suma es: {numero_1 + numero_2}")

#Resta
print("La resta es: " + str(numero_1 - numero_2))

#Multiplicación
print(f"La multiplicación es: {numero_1 * numero_2}")

#División
print(f"La división es: {numero_1 / numero_2}")